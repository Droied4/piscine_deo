int	ft_str_is_alpha(char *str)
{
	int	cpr1;

	if (*str == '\0')
		return (1);
	cpr1 = 0;
	while (*str != '\0')
	{
		if ((*str >= 'A' && *str <= 'Z') || (*str >= 'a' && *str <= 'z'))
		{
			cpr1 = 1;
		}
		else
		{
			return (0);
		}
		if (cpr1 == 1)
		{
			str++;
		}
	}
	return (1);
}
