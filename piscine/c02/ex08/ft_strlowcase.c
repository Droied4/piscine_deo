char	*ft_strlowcase(char *str)
{
	int		i;
	char	lower;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			lower = str[i] + 32;
			str[i] = lower;
		}
		i++;
	}
	return (str);
}
